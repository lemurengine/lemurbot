    public function isolatedRun(){

        DB::beginTransaction();
        try{
            $this->run();
            DB::commit();
        }catch(Throwable $exception){
            DB::rollback();
            $this->displayMessage("An error occurred: ".$exception->getMessage()." ".basename($exception->getFile())."[".$exception->getLine()."]", "error");
            return 0;
        }
    }


    public function run(){


            $this->displayMessage("Installing Lemur Engine Word Lists", "title");

            $this->setupIds();
            //create the word transformations for the app
            $this->createWordTransformations();
            //create the word spelling groups for the app
            $this->createWordSpellingGroups();
            //create the text talk expansions for the app
            $this->createTxtSpellings();
            //create the default spelling corrections for the app
            $this->createDefaultSpellings();


    }


    public function createWordTransformations()
    {
        $newCount = 0;
        $existCount = 0;
        $wordTransformationArr = $this->getWordTransformationList();
        foreach($wordTransformationArr as $data){
            $wordTransformations = WordTransformation::firstOrCreate(
                ['language_id' =>  $this->langId,
                'first_person_form' => $data[0],
                'second_person_form' => $data[1],
                'third_person_form' => $data[2],
                'third_person_form_female' => $data[3],
                'third_person_form_male' => $data[4],
                'is_master' => $data[5]]
            );
            if($wordTransformations->wasRecentlyCreated){
                $newCount++;
            }else{
                $existCount++;
            }
        }
        if($newCount>0){
            $this->displayMessage($newCount." word transformations created", "success");
        }
        if($existCount>0){
            $this->displayMessage($existCount." word transformations created", "notice");
        }
    }


    public function createWordSpellingGroups()
    {

            $wordSpellingGroup = WordSpellingGroup::firstOrCreate(
                ['slug' => $this->defaultSpellingSetSlug, 'user_id' => $this->getUserId()],
                [
                    'language_id' =>  $this->langId,
                    'name' => 'Default spelling set',
                    'description' => 'Spelling corrections for common mistakes',
                    'is_master' => 1
                ]
            );

            if($wordSpellingGroup->wasRecentlyCreated){
                $this->displayMessage("Default spelling set group created", "success");
            }else{
                $this->displayMessage("Default spelling set group exists", "notice");
            }


        $wordSpellingGroup = WordSpellingGroup::firstOrCreate(
            ['slug' => $this->textTalkExpansionsSlug, 'user_id' => $this->getUserId()],
            [
                'language_id' =>  $this->langId,
                'name' => 'Text Talk Expansions',
                'description' => 'Expand out common text talk spellings',
                'is_master' => 1
            ]
            );

        if($wordSpellingGroup->wasRecentlyCreated){
            $this->displayMessage("Text Talk Expansions created", "success");
        }else{
            $this->displayMessage("Text Talk Expansions exists", "notice");
        }


    }

    public function createDefaultSpellings(){

        $newCount = 0;
        $existCount = 0;
        $wordTxtSpellingArr = $this->getDefaultSpellingList();

        $group = WordSpellingGroup::where('user_id', $this->getUserId())->where( 'slug', $this->defaultSpellingSetSlug)->firstOrFail();

        foreach($wordTxtSpellingArr as $data){

            $wordSpelling = WordSpelling::firstOrCreate(
                ['word_spelling_group_id' =>  $group->id, 'word' => $data[0]],
                ['replacement' => $data[1]]
            );
            if($wordSpelling->wasRecentlyCreated){
                $newCount++;
            }else{
                $existCount++;
            }
        }

        if($newCount>0){
            $this->displayMessage($newCount." default spelling corrections created", "success");
        }
        if($existCount>0){
            $this->displayMessage($existCount." default spelling corrections already exist", "notice");
        }
    }

    public function createTxtSpellings(){

        $newCount = 0;
        $existCount = 0;
        $wordTxtSpellingArr = $this->getWordTextSpellingList();
        $group = WordSpellingGroup::where('user_id', $this->getUserId())->where( 'slug', $this->textTalkExpansionsSlug)->firstOrFail();
        foreach($wordTxtSpellingArr as $data){
            $wordSpelling = WordSpelling::firstOrCreate(
                ['word_spelling_group_id' =>  $group->id, 'word' => $data[0]],
                ['replacement' => $data[1]]
            );
            if($wordSpelling->wasRecentlyCreated){
                $newCount++;
            }else{
                $existCount++;
            }
        }
        if($newCount>0){
            $this->displayMessage($newCount." text talk expansions created", "success");
        }

        if($existCount>0){
            $this->displayMessage($existCount." text talk expansions created", "notice");
        }

    }

